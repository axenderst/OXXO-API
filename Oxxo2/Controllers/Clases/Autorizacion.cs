namespace Oxxo2.Controllers.Clases
{
    public class Autorizacion
    {
        public bool ValidaPermisos(string usuario) {
            bool Permitido;

            // realizar la validacion de los permisos de usuario
            if (usuario == "admin")
            {
                Permitido = true; //Temporal hasta tener la definición de los roles Usados en OXXO.
            }
            else {
                Permitido = false;
            }
                                 

            if (Permitido)
            {
                return true;
            }
            else
            {
                return false;
            }
        }

    }
}
